public class Sonata extends Car
{
    @Override
    public void start()
    {
        System.out.println("Sonata Turn on the Car");
    }

    @Override
    public void drive()
    {
        System.out.println("Sonata Drive the Car");
    }

    @Override
    public void stop()
    {
        System.out.println("Sonata Stop the Car");
    }

    @Override
    public void turnoff()
    {
        System.out.println("Sonata Turn off the Car");
    }

}
